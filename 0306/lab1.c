#include <stdio.h>
int main(void)
{
	int xData; // 첫 번째 정수를 저장할 변수
	int yData; // 두 번째 정수를 저장할 변수
	int sum, diff, mul, div; // 두 정수 간의 연산의 결과를 저장하는 변수
	xData = 20; // 변수 x에 2을 저장
	yData = 10; // 변수 y에 10을 저장
	sum = xData + yData; // 변수 sum에 (x+y)의 결과를 저장
	diff = xData - yData; // 변수 diff에 (x-y)의 결과를 저장
	mul = xData * yData; // 변수 mul에 (x*y)의 결과를 저장
	div = xData / yData; // 변수 div에 (x/y)의 결과를 저장

	printf("두수의 합: %i\n", sum); // 변수 sum의 값을 화면에 출력
	printf("두수의 차: %i\n", diff);// 변수 diff의 값을 화면에 출력
	printf("두수의 곱: %i\n", mul); // 변수 mul의 값을 화면에 출력
	printf("두수의 몫: %i\n", div); // 변수 div의 값을 화면에 출력
	return 0;
}